//Mostrar o resultado da multiplicação de dois números

int main()
{
    //inicializar as duas notas
    int n1, n2, resultado;

    //receber as duas notas
    printf("Digite as duas notas: ");
    scanf("%d %d", &n1, &n2);

    //mostrar o resultado da multiplicação
    resultado = n1 * n2;
    printf("Multiplicacao = %d", resultado);

    return 0;


}
