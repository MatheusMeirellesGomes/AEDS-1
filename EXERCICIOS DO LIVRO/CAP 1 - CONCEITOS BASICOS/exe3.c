//Mostrar o resultado da divisão de dois números
int main()
{
   //inicializar as duas notas
   int n1, n2, divisao;

   //receber as duas notas
   printf("Digitar as duas notas: ");
   scanf("%d %d", &n1, &n2);

   //fazer a divisão
   divisao = n1 / n2;
   printf("O resultado da divisao eh: %d", divisao);

    return 0;


}